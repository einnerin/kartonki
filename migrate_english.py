def classify(name: str) -> tuple[str, int]:
    """Возвращает (topic, level) для имени набора."""
    n = name.lower()

    # Явные маркеры уровня
    level = None
    if "(c1)" in n or " c1" in n:
        level = 4
    elif "(продвинутый)" in n or " продвинутый" in n:
        level = 3
    elif ": углубл" in n or "углубл" in n:
        level = 3
    elif "(базовый)" in n or ": основы" in n:
        level = 1

    for kw, topic in TOPIC_RULES:
        if kw in n:
            return (topic, level if level else 1)

    return ("Повседневная жизнь", level if level else 1)


def find_blocks(content: str) -> list[tuple[int, int, str]]:
    """Ищет WordSetEntity(...) блоки, учитывая кавычки и вложенные скобки.
    Возвращает список (start, end, body) — start/end позиции всего блока WordSetEntity(...)."""
    blocks = []
    pattern = re.compile(r"WordSetEntity\s*\(")
    for m in pattern.finditer(content):
        start = m.start()
        i = m.end()  # после открывающей (
        depth = 1
        in_str = False
        esc = False
        while i < len(content) and depth > 0:
            ch = content[i]
            if esc:
                esc = False
            elif ch == "\\":
                esc = True
            elif ch == '"':
                in_str = not in_str
            elif not in_str:
                if ch == "(":
                    depth += 1
                elif ch == ")":
                    depth -= 1
                    if depth == 0:
                        blocks.append((start, i + 1, content[m.end():i]))
                        break
            i += 1
    return blocks


def process_file(path: str) -> list[tuple[int, str, str, int]]:
    content = open(path, encoding="utf-8").read()
    blocks = find_blocks(content)
    changes = []
    # Обрабатываем справа-налево, чтобы не сдвигать позиции
    for start, end, body in reversed(blocks):
        if re.search(r"\btopic\s*=", body) and re.search(r"\blevel\s*=", body):
            continue
        id_m = re.search(r"\bid\s*=\s*(\d+)", body)
        name_m = re.search(r'\bname\s*=\s*"((?:\\"|[^"])*)"', body)
        if not id_m or not name_m:
            continue
        set_id = int(id_m.group(1))
        name = name_m.group(1)
        topic, level = classify(name)
        trimmed = body.rstrip().rstrip(",").rstrip()
        new_body = f'{trimmed}, topic = "{topic}", level = {level}'
        content = content[:start] + f"WordSetEntity({new_body})" + content[end:]
        changes.append((set_id, name, topic, level))
    if changes:
        open(path, "w", encoding="utf-8").write(content)
    return list(reversed(changes))


def main():
    files = sorted(glob.glob("app/src/main/java/com/example/kartonki/data/WordDataEnglish*.kt"))
    total = 0
    for f in files:
        changes = process_file(f)
        total += len(changes)
        print(f"{f.split(chr(92))[-1]}: +{len(changes)} наборов")
        for set_id, name, topic, level in changes:
            print(f"  {set_id}\t{name}\t→ {topic} / L{level}")
    print(f"\nИтого обновлено: {total} наборов")


if __name__ == "__main__":
    sys.stdout.reconfigure(encoding="utf-8")
    main()
